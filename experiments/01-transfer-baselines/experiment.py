"""experiment.py - transfer learning on CIFAR-10.

Generated by experiment-plan. Runs on the RunPod persistent pod over SSH
(engine.skills.run_experiments invokes ``python experiment.py``).

Compares three transfer-learning strategies for ResNet-18 on CIFAR-10:
  (A) Scratch:    random init, train all weights end-to-end.
  (B) Linear:     ImageNet-pretrained backbone frozen; train only the
                  re-initialised final classifier (linear probe).
  (C) Finetune:   ImageNet-pretrained backbone + classifier, train all
                  weights end-to-end.

Per seed we train each of A/B/C for the same number of epochs and
record test top-1 accuracy. The primary metric is the linear-probe
accuracy; all three numbers are reported in summary.json. No plotting
here - figures come from the figures stage downstream.
"""

