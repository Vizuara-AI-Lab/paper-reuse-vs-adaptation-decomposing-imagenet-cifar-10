import random
import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim
import torchvision  # noqa: F401  (kept for ResNet18 weights below)
import torchvision.transforms as T
from torchvision.models import resnet18, ResNet18_Weights
from torch.utils.data import DataLoader, Dataset
from datasets import load_dataset

ART = pathlib.Path("/workspace/artifacts")
(ART / "data").mkdir(parents=True, exist_ok=True)
(ART / "figures").mkdir(parents=True, exist_ok=True)
(ART / "tables").mkdir(parents=True, exist_ok=True)
DATA_ROOT = pathlib.Path("/workspace/data")
DATA_ROOT.mkdir(parents=True, exist_ok=True)

EPOCHS = int(os.environ.get("EPOCHS", "5"))
BATCH_SIZE = int(os.environ.get("BATCH_SIZE", "128"))
LR_FINETUNE = float(os.environ.get("LR_FINETUNE", "1e-3"))
LR_LINEAR = float(os.environ.get("LR_LINEAR", "1e-2"))
LR_SCRATCH = float(os.environ.get("LR_SCRATCH", "1e-3"))
INPUT_SIZE = int(os.environ.get("INPUT_SIZE", "224"))
NUM_WORKERS = int(os.environ.get("NUM_WORKERS", "4"))

DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
CIFAR_CLASSES = 10

class _HFCIFARWrapper(Dataset):
    """Wrap a HuggingFace cifar10 split as a torch Dataset.

    The torchvision CIFAR10 loader pulls from www.cs.toronto.edu, which
    intermittently returns 503 from the RunPod datacenter. We use the
    `uoft-cs/cifar10` HF dataset instead — it's PNG/PIL data with `img`
    + `label` columns, served from HF's CDN. Transforms are applied on
    the fly to match the torchvision pipeline.
    """

    def __init__(self, hf_split, transform):
        self._split = hf_split
        self._transform = transform

    def __len__(self):
        return len(self._split)

    def __getitem__(self, idx):
        row = self._split[int(idx)]
        img = row["img"]
        label = int(row["label"])
        if self._transform is not None:
            img = self._transform(img)
        return img, label


def _make_loaders():
    weights = ResNet18_Weights.IMAGENET1K_V1
    norm = T.Normalize(
        mean=weights.transforms().mean,
        std=weights.transforms().std,
    )
    train_tf = T.Compose([
        T.Resize(INPUT_SIZE),
        T.RandomHorizontalFlip(),
        T.ToTensor(),
        norm,
    ])
    eval_tf = T.Compose([T.Resize(INPUT_SIZE), T.ToTensor(), norm])
    print("[data] loading CIFAR-10 via HuggingFace (uoft-cs/cifar10)", flush=True)
    hf = load_dataset("uoft-cs/cifar10", cache_dir=str(DATA_ROOT / "hf_cache"))
    train_set = _HFCIFARWrapper(hf["train"], train_tf)
    test_set = _HFCIFARWrapper(hf["test"], eval_tf)
    print(
        "[data] train=" + str(len(train_set))
        + " test=" + str(len(test_set)),
        flush=True,
    )
    train_loader = DataLoader(
        train_set, batch_size=BATCH_SIZE, shuffle=True,
        num_workers=NUM_WORKERS, pin_memory=True, drop_last=True,
    )
    test_loader = DataLoader(
        test_set, batch_size=256, shuffle=False,
        num_workers=NUM_WORKERS, pin_memory=True,
    )
    return train_loader, test_loader


def _build_model(condition):
    if condition == "scratch":
        m = resnet18(weights=None)
    else:
        m = resnet18(weights=ResNet18_Weights.IMAGENET1K_V1)
    in_feat = m.fc.in_features
    m.fc = nn.Linear(in_feat, CIFAR_CLASSES)
    if condition == "linear":
        for p in m.parameters():
            p.requires_grad = False
        for p in m.fc.parameters():
            p.requires_grad = True
    return m


def _make_optimizer(model, condition):
    params = [p for p in model.parameters() if p.requires_grad]
    if condition == "scratch":
        return optim.Adam(params, lr=LR_SCRATCH)
    if condition == "linear":
        return optim.SGD(params, lr=LR_LINEAR, momentum=0.9, weight_decay=0.0)
    return optim.Adam(params, lr=LR_FINETUNE)


@torch.no_grad()
def _evaluate(model, loader):
    model.eval()
    correct = total = 0
    for xb, yb in loader:
        xb = xb.to(DEVICE, non_blocking=True)
        yb = yb.to(DEVICE, non_blocking=True)
        logits = model(xb)
        pred = logits.argmax(dim=1)
        correct += (pred == yb).sum().item()
        total += yb.size(0)
    return correct / max(total, 1)


def _train_condition(condition, seed, train_loader, test_loader):
    torch.manual_seed(seed)
    np.random.seed(seed)
    random.seed(seed)
    model = _build_model(condition).to(DEVICE)
    optimizer = _make_optimizer(model, condition)
    criterion = nn.CrossEntropyLoss()
    train_curve, test_curve = [], []
    t0 = time.time()
    for epoch in range(EPOCHS):
        model.train()
        if condition == "linear":
            for mod in model.modules():
                if isinstance(mod, nn.BatchNorm2d):
                    mod.eval()
        running_loss = 0.0
        running_n = 0
        running_correct = 0
        for xb, yb in train_loader:
            xb = xb.to(DEVICE, non_blocking=True)
            yb = yb.to(DEVICE, non_blocking=True)
            optimizer.zero_grad(set_to_none=True)
            logits = model(xb)
            loss = criterion(logits, yb)
            loss.backward()
            optimizer.step()
            running_loss += loss.item() * yb.size(0)
            running_correct += (logits.argmax(1) == yb).sum().item()
            running_n += yb.size(0)
        train_loss = running_loss / max(running_n, 1)
        train_acc = running_correct / max(running_n, 1)
        test_acc = _evaluate(model, test_loader)
        train_curve.append({"epoch": epoch + 1, "loss": train_loss, "acc": train_acc})
        test_curve.append({"epoch": epoch + 1, "acc": test_acc})
        msg = (
            "[" + condition + " seed=" + str(seed) + "] "
            + "epoch=" + str(epoch + 1) + "/" + str(EPOCHS) + " "
            + "train_loss=" + ("%.4f" % train_loss) + " "
            + "train_acc=" + ("%.4f" % train_acc) + " "
            + "test_acc=" + ("%.4f" % test_acc)
        )
        print(msg, flush=True)
    elapsed = time.time() - t0
    final_test_acc = test_curve[-1]["acc"]
    peak_mem = (
        torch.cuda.max_memory_allocated() / 1e9
        if torch.cuda.is_available()
        else 0.0
    )
    if torch.cuda.is_available():
        torch.cuda.reset_peak_memory_stats()
    return {
        "condition": condition,
        "final_test_acc": final_test_acc,
        "train_curve": train_curve,
        "test_curve": test_curve,
        "training_time_s": elapsed,
        "peak_gpu_memory_gb": peak_mem,
    }


def run_seed(seed):
    if os.environ.get("EXPERIMENT_STUB"):
        return {
            "seed": seed,
            "primary_metric_name": "linear_probe_test_acc",
            "primary_metric": 0.5,
            "scratch_test_acc": 0.4,
            "linear_test_acc": 0.5,
            "finetune_test_acc": 0.6,
            "training_time_s": 0.0,
            "peak_gpu_memory_gb": 0.0,
        }
    train_loader, test_loader = _make_loaders()
    by_condition = {}
    total_time = 0.0
    peak_mem = 0.0
    for condition in ("scratch", "linear", "finetune"):
        rec = _train_condition(condition, seed, train_loader, test_loader)
        by_condition[condition] = rec
        total_time += rec["training_time_s"]
        peak_mem = max(peak_mem, rec["peak_gpu_memory_gb"])
    result = {
        "seed": seed,
        "primary_metric_name": "linear_probe_test_acc",
        "primary_metric": by_condition["linear"]["final_test_acc"],
        "scratch_test_acc": by_condition["scratch"]["final_test_acc"],
        "linear_test_acc": by_condition["linear"]["final_test_acc"],
        "finetune_test_acc": by_condition["finetune"]["final_test_acc"],
        "scratch_curve": by_condition["scratch"]["test_curve"],
        "linear_curve": by_condition["linear"]["test_curve"],
        "finetune_curve": by_condition["finetune"]["test_curve"],
        "scratch_train_curve": by_condition["scratch"]["train_curve"],
        "linear_train_curve": by_condition["linear"]["train_curve"],
        "finetune_train_curve": by_condition["finetune"]["train_curve"],
        "training_time_s": total_time,
        "peak_gpu_memory_gb": peak_mem,
    }
    pm = result["primary_metric"]
    print(
        "RESULT: metric=" + ("%.4f" % pm) + " seed=" + str(seed),
        flush=True,
    )
    return result


def _save_table(per_seed):
    rows = ["condition,mean_test_acc,std_test_acc,n_seeds"]
    for c in ("scratch", "linear", "finetune"):
        accs = [p[c + "_test_acc"] for p in per_seed]
        mean = float(sum(accs) / len(accs))
        var = sum((a - mean) ** 2 for a in accs) / len(accs)
        std = var ** 0.5
        rows.append(
            c + "," + ("%.4f" % mean) + "," + ("%.4f" % std)
            + "," + str(len(accs))
        )
    (ART / "tables" / "transfer_results.csv").write_text(
        "\n".join(rows) + "\n"
    )


SEEDS = [int(x) for x in os.environ.get("SEEDS", "0,1,2").split(",")]
results = []
metrics_jsonl = ART / "data" / "metrics.jsonl"
with metrics_jsonl.open("a", encoding="utf-8") as fh:
    for seed in SEEDS:
        r = run_seed(seed)
        results.append(r)
        fh.write(json.dumps(r) + "\n")

primary = [r["primary_metric"] for r in results]
mean = float(sum(primary) / len(primary))
var = sum((p - mean) ** 2 for p in primary) / len(primary)
std = var ** 0.5


def _agg(field):
    vals = [r[field] for r in results]
    m = float(sum(vals) / len(vals))
    v = sum((x - m) ** 2 for x in vals) / len(vals)
    return {"mean": m, "std": v ** 0.5, "values": vals}


summary = {
    "primary_metric": {
        "name": results[0]["primary_metric_name"],
        "mean": mean,
        "std": std,
        "n_seeds": len(primary),
    },
    "per_condition": {
        "scratch": _agg("scratch_test_acc"),
        "linear": _agg("linear_test_acc"),
        "finetune": _agg("finetune_test_acc"),
    },
    "per_seed": results,
    "config": {
        "epochs": EPOCHS,
        "batch_size": BATCH_SIZE,
        "lr_scratch": LR_SCRATCH,
        "lr_linear": LR_LINEAR,
        "lr_finetune": LR_FINETUNE,
        "input_size": INPUT_SIZE,
        "backbone": "resnet18",
        "pretrained_weights": "ResNet18_Weights.IMAGENET1K_V1",
        "dataset": "CIFAR-10",
    },
}
(ART / "data" / "summary.json").write_text(json.dumps(summary, indent=2))
if not os.environ.get("EXPERIMENT_STUB"):
    _save_table(results)
print("SUMMARY: " + json.dumps(summary), flush=True)